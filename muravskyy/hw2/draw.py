from turtle import*
setup(200,300)
ht()
speed(0)
pensize(3)
begin_fill()
circle(50)
fillcolor('yellow')
end_fill()
penup()
pensize(2)
sety(20)
pendown()
circle(50,50)
circle(50,-100)
penup()
pensize(8)
goto(15,60)
pendown()
left(110)
circle(20,60)
goto(15,60)
circle(-20,60)
penup()
goto(-15,60)
pendown()
circle(20,60)
goto(-15,60)
circle(-20,60)
done()
